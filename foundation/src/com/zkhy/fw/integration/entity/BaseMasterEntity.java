package com.zkhy.fw.integration.entity;

import java.util.Date;

/**
 *
 * マスタDB用基底エンティティクラス
 *
 * @author guozhiqiang
 *
 */
@SuppressWarnings("serial")
public abstract class BaseMasterEntity extends BaseEntity {
    // 作成者
    private String createBy;
    // 作成日時
    private Date createDt;
    // 最終更新者
    private String lastUpdOp;
    // 最終更新日時
    private Date lastUpdDt;
    /**
     * createByを返却する。
     *
     * @return createBy
     */
    public String getCreateBy() {
        return createBy;
    }
    /**
     * createByをセットする。
     * @param createBy createBy
     */
    public void setCreateBy(String createBy) {
        this.createBy = createBy;
    }
    /**
     * createDtを返却する。
     *
     * @return createDt
     */
    public Date getCreateDt() {
        return createDt;
    }
    /**
     * createDtをセットする。
     * @param createDt createDt
     */
    public void setCreateDt(Date createDt) {
        this.createDt = createDt;
    }
    /**
     * lastUpdOpを返却する。
     *
     * @return lastUpdOp
     */
    public String getLastUpdOp() {
        return lastUpdOp;
    }
    /**
     * lastUpdOpをセットする。
     * @param lastUpdOp lastUpdOp
     */
    public void setLastUpdOp(String lastUpdOp) {
        this.lastUpdOp = lastUpdOp;
    }
    /**
     * lastUpdDtを返却する。
     *
     * @return lastUpdDt
     */
    public Date getLastUpdDt() {
        return lastUpdDt;
    }
    /**
     * lastUpdDtをセットする。
     * @param lastUpdDt lastUpdDt
     */
    public void setLastUpdDt(Date lastUpdDt) {
        this.lastUpdDt = lastUpdDt;
    }


}
