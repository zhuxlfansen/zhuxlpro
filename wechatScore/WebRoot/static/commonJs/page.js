function goPage(page,pageCount){
	if(page<1){
		$("#curpage").attr("value",1);
	}else if(page>pageCount){
		$("#curpage").attr("value",pageCount);
	}else{
		$("#curpage").attr("value",page);
	}
	$("#form1").submit();
}

