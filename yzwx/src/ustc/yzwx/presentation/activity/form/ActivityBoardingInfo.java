package ustc.yzwx.presentation.activity.form;

import java.io.Serializable;

public class ActivityBoardingInfo implements Serializable {

	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = -1996322210265104267L;
	private Integer id;
	private int activityId;
	private String boardingInfo;
	private int totalStu;
	
	public int getTotalStu() {
		return totalStu;
	}
	public void setTotalStu(int totalStu) {
		this.totalStu = totalStu;
	}
	public int getActivityId() {
		return activityId;
	}
	public void setActivityId(int activityId) {
		this.activityId = activityId;
	}
	public String getBoardingInfo() {
		return boardingInfo;
	}
	public void setBoardingInfo(String boardingInfo) {
		this.boardingInfo = boardingInfo;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
}
